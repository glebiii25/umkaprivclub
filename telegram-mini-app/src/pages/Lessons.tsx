export function Lessons() {
  const { id } = useParams<{ id: string }>();
  const [searchParams] = useSearchParams();
  const location = useLocation();

  const level = searchParams.get('level') ?? '';
  const program = searchParams.get('program') ?? '';
  const topic = searchParams.get('topic') ?? '';
  const exerciseParam = searchParams.get('exercise') ?? '';

  const moduleId = id ?? '';
  const topicId = topic as TopicId | '';
  const isModule2WithTopic =
    moduleId === '2' && topicId && topicId in MODULE2_LESSONS;
  const content = isModule2WithTopic ? MODULE2_LESSONS[topicId as TopicId] : null;
  const exercises = isModule2WithTopic ? MODULE2_EXERCISES[topicId as TopicId] : [];
  const exerciseIndex = exerciseParam === '' ? -1 : parseInt(exerciseParam, 10);
  const hasValidExercise =
    !Number.isNaN(exerciseIndex) &&
    exerciseIndex >= 0 &&
    exerciseIndex < exercises.length;
  const currentExercise = hasValidExercise ? exercises[exerciseIndex] : null;

  if (currentExercise) {
    return (
      <ExerciseExecutionView
        moduleId={moduleId}
        topicId={topicId as TopicId}
        exercise={currentExercise}
        exerciseIndex={exerciseIndex}
        totalExercises={exercises.length}
      />
    );
  }

  if (content) {
    return <LessonView moduleId={moduleId} topicId={topicId as TopicId} content={content} />;
  }

  return (
    <div
      className="relative flex min-h-screen flex-col items-center justify-center safe-area-padding p-4"
      style={{
        backgroundColor: BG_DARK,
        color: 'rgb(255,255,255)',
      }}
    >
      <div className="absolute left-4 top-4">
        <BackButton />
      </div>
      <p className="text-lg font-medium">Уроки — модуль {id ?? '—'}</p>
      {level && <p className="mt-2 text-sm opacity-90">Уровень: {level}</p>}
      {program && <p className="mt-1 text-sm opacity-90">Программа: {program}</p>}
      {topic && <p className="mt-1 text-sm opacity-90">Тема: {topic}</p>}
      {exerciseParam && <p className="mt-1 text-sm opacity-90">Упражнение: {exerciseParam}</p>}
      <Link
        to={`/module/${id ?? ''}`}
        className="mt-4 cursor-pointer text-sm underline opacity-90 transition-opacity hover:opacity-100"
      >
        К модулю
      </Link>
      <Link
        to="/"
        className="mt-2 cursor-pointer text-sm underline opacity-90 transition-opacity hover:opacity-100"
      >
        На главную
      </Link>
    </div>
  );
}
