function ExerciseExecutionView({
  moduleId,
  topicId,
  exercise,
  exerciseIndex,
  totalExercises,
}: {
  moduleId: string;
  topicId: TopicId;
  exercise: Module2ExerciseItem;
  exerciseIndex: number;
  totalExercises: number;
}) {
  const location = useLocation();
  const topicPageUrl = `/module/${moduleId}/lessons?topic=${topicId}`;
  const prevUrl =
    exerciseIndex > 0
      ? `/module/${moduleId}/lessons?topic=${topicId}&exercise=${exerciseIndex - 1}`
      : topicPageUrl;
  const nextUrl =
    exerciseIndex < totalExercises - 1
      ? `/module/${moduleId}/lessons?topic=${topicId}&exercise=${exerciseIndex + 1}`
      : undefined;
  const hasNext = nextUrl != null;

  const favoriteItem = {
    id: `${topicId}-${exerciseIndex}`,
    title: exercise.title,
    href: `/module/${moduleId}/lessons?topic=${topicId}&exercise=${exerciseIndex}`,
  };

  const [mediaHeight, setMediaHeight] = useState(180);
  useEffect(() => {
    setMediaHeight(180);
  }, [exerciseIndex, topicId]);

  const handleMediaLoaded = (width: number, height: number) => {
    if (height > width) setMediaHeight(340);
  };

  return (
    <div
      className="min-h-screen safe-area-padding pb-12"
      style={{ backgroundColor: BG_DARK, color: 'rgb(255,255,255)' }}
    >
      <div className="mx-auto max-w-[440px] px-10 pt-[20px]">
        <div className="flex items-center justify-between gap-2">
          <BackButton />
          <FavoriteButton item={favoriteItem} className="h-10 w-10 shrink-0 rounded-full" />
        </div>
        <div
          className="mt-8 w-[345px] max-w-full rounded-lg"
          style={{
            height: mediaHeight,
            backgroundColor: exercise.imageUrl ? 'transparent' : PHOTO_PLACEHOLDER,
          }}
        >
          {exercise.imageUrl ? (
            /\.(mp4|mov)$/i.test(exercise.imageUrl) ? (
              <video
                src={getMediaUrl(exercise.imageUrl, { basePrefix: getMediaBaseUrl(location.pathname) })}
                className="h-full w-full rounded-lg object-contain"
                autoPlay
                loop
                muted
                playsInline
                aria-hidden
                onLoadedMetadata={(e) => {
                  const v = e.currentTarget;
                  if (v.videoWidth && v.videoHeight) handleMediaLoaded(v.videoWidth, v.videoHeight);
                }}
                onError={(e) => {
                  const fallback = getMediaFallbackUrl(exercise.imageUrl!, { basePrefix: getMediaBaseUrl(location.pathname) });
                  if (fallback.startsWith('http')) e.currentTarget.src = fallback;
                }}
              />
            ) : (
              <img
                src={getMediaUrl(exercise.imageUrl, { basePrefix: getMediaBaseUrl(location.pathname) })}
                alt=""
                className="h-full w-full rounded-lg object-contain"
                onLoad={(e) => {
                  const img = e.currentTarget;
                  if (img.naturalWidth && img.naturalHeight) handleMediaLoaded(img.naturalWidth, img.naturalHeight);
                }}
                onError={(e) => {
                  const fallback = getMediaFallbackUrl(exercise.imageUrl!, { basePrefix: getMediaBaseUrl(location.pathname) });
                  if (fallback.startsWith('http')) e.currentTarget.src = fallback;
                }}
              />
            )
          ) : null}
        </div>

        <section className="mt-10 w-[352px] max-w-full">
          <h1
            className="text-[19px] font-medium leading-tight"
            style={{ color: GREEN, fontFamily: '"SF Pro", -apple-system, sans-serif' }}
          >
            {exercise.title}
          </h1>
          <h2 className="mt-6 text-[19px] leading-tight text-white">
            {exercise.techniqueTitle}
          </h2>
          <p className="mt-4 text-[19px] leading-tight text-white">
            {exercise.techniqueText}
          </p>
        </section>

        {/* Навигация как в первом модуле */}
        <div
          className="mt-[83px] mx-auto flex w-[280px] max-w-full items-center justify-between"
        >
          <Link
            to={prevUrl}
            className="flex h-10 w-10 items-center justify-center rounded-full border transition-opacity hover:opacity-90"
            style={{
              borderColor: NAV_BORDER,
              backgroundColor: NAV_BG,
              color: NAV_TEXT,
            }}
            aria-label={exerciseIndex > 0 ? 'Предыдущее упражнение' : 'К уроку'}
          >
            <ChevronLeftIcon />
          </Link>

          <Link
            to="/"
            className="flex h-10 items-center gap-2 rounded-[8px] border px-3 transition-opacity hover:opacity-90"
            style={{
              backgroundColor: NAV_BG,
              borderColor: NAV_BORDER_PILL,
              color: NAV_TEXT,
            }}
          >
            <span className="text-[16px]">Главная</span>
          </Link>

          {hasNext ? (
            <Link
              to={nextUrl}
              className="flex h-10 w-10 items-center justify-center rounded-full border transition-opacity hover:opacity-90"
              style={{
                borderColor: NAV_BORDER,
                backgroundColor: NAV_BG,
                color: NAV_TEXT,
              }}
              aria-label="Следующее упражнение"
            >
              <ChevronRightIcon />
            </Link>
          ) : (
            <Link
              to={topicPageUrl}
              className="flex h-10 w-10 items-center justify-center rounded-full border transition-opacity hover:opacity-90"
              style={{
                borderColor: NAV_BORDER,
                backgroundColor: NAV_BG,
                color: NAV_TEXT,
              }}
              aria-label="К уроку"
            >
              <ChevronRightIcon />
            </Link>
          )}
        </div>
      </div>
    </div>
  );
}

