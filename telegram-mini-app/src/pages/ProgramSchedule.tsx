export default function ProgramSchedule() {
  const { moduleId, programId, levelId } = useParams<{ moduleId: string; programId: string; levelId: string }>();
  const [searchParams] = useSearchParams();
  const week = searchParams.get('week');
  const backUrl = moduleId && programId && levelId ? `/module/${moduleId}/program/${programId}/level/${levelId}` : '/';

  const isWeek34 = week === '3-4';
  const isWeek56 = week === '5-6';
  const isLevel1 = programId === 'fht-men' && levelId === '1';
  const isLevel2 = programId === 'fht-men' && levelId === '2';
  const isLevel3 = programId === 'fht-men' && levelId === '3';
  const isWomen1 = programId === 'fht-women' && levelId === '1';
  const isWomen2 = programId === 'fht-women' && levelId === '2';
  const isWomen3 = programId === 'fht-women' && levelId === '3';
  const days = isWomen3
    ? isWeek56
      ? FHT_WOMEN_3_WEEK_56
      : isWeek34
        ? FHT_WOMEN_3_WEEK_34
        : FHT_WOMEN_3_WEEK_12
    : isWomen2
      ? isWeek56
        ? FHT_WOMEN_2_WEEK_56
        : isWeek34
          ? FHT_WOMEN_2_WEEK_34
          : FHT_WOMEN_2_WEEK_12
      : isWomen1
      ? isWeek56
        ? FHT_WOMEN_1_WEEK_56
        : isWeek34
          ? FHT_WOMEN_1_WEEK_34
          : FHT_WOMEN_1_WEEK_12
      : isLevel1
      ? isWeek56
        ? FHT_MEN_1_WEEK_56
        : isWeek34
          ? FHT_MEN_1_WEEK_34
          : FHT_MEN_1_WEEK_12
      : isLevel2
        ? isWeek56
          ? FHT_MEN_2_WEEK_56
          : isWeek34
            ? FHT_MEN_2_WEEK_34
            : FHT_MEN_2_WEEK_12
        : isLevel3 && isWeek56
          ? FHT_MEN_3_WEEK_56
          : isLevel3 && isWeek34
            ? FHT_MEN_3_WEEK_34
            : isLevel3 && !isWeek34 && !isWeek56
              ? FHT_MEN_3_WEEK_12
              : null;
  const title = isWomen3 && !isWeek34 && !isWeek56
    ? 'Неделя 1-2 Контроль и координация'
    : isWomen3 && isWeek34
      ? 'Неделя 3-4 Динамика и атлетизм'
      : isWomen3 && isWeek56
        ? 'Неделя 5-6 Flow и свобода движения'
        : isWomen2 && !isWeek34 && !isWeek56
          ? 'Неделя 1-2 Введение в силовую работу'
          : isWomen2 && isWeek34
            ? 'Неделя 3-4 Сила и динамическая устойчивость'
            : isWomen2 && isWeek56
              ? 'Неделя 5-6 Контроль силы и взрывная динамика'
              : isWomen1 && !isWeek34 && !isWeek56
          ? 'Неделя 1-2'
          : isWomen1 && isWeek34
            ? 'Неделя 3-4 Развитие силы и структуры тела'
            : isWomen1 && isWeek56
              ? 'Неделя 5-6 Контроль, Динамика и Устойчивость'
              : isLevel3 && isWeek56
          ? 'Неделя 5-6 Гибридный атлетизм'
          : isLevel3 && isWeek34
            ? 'Неделя 3-4 Сила, координация и взрыв'
            : isLevel3 && !isWeek34 && !isWeek56
              ? 'Неделя 1-2 Контроль и техника в движении'
              : isLevel2 && isWeek56
          ? 'Неделя 5-6 Пиковая сила'
          : isLevel2 && isWeek34
            ? 'Неделя 3-4 Развитие силы и новых движений'
            : isLevel2 && !isWeek34 && !isWeek56
              ? 'Неделя 1-2 Введение в силовую работу'
              : isWeek56
                ? 'Неделя 5-6 Контроль и устойчивость'
                : isWeek34
                  ? 'Неделя 3-4 Формирование силы'
                  : 'Неделя 1-2 Адаптация и Техника';

  return (
    <div
      className="min-h-screen safe-area-padding pb-12"
      style={{ backgroundColor: BG_DARK, color: 'rgb(255,255,255)' }}
    >
      <div className="mx-auto max-w-[440px] px-[25px] pt-[20px]">
        <BackButton />
        <h1
          className="mt-8 mb-[46px] text-[19px] leading-tight"
          style={{
            color: PURPLE,
            fontFamily: '"SF Pro", -apple-system, BlinkMacSystemFont, sans-serif',
          }}
        >
          {days ? title : 'График'}
        </h1>

        {days ? (
          <div className="flex flex-col gap-[68px]">
            {days.map((day) => (
              <div key={day.dayTitle} className="w-[382px] max-w-full">
                <div
                  className="flex items-start justify-between border-b pb-2"
                  style={{ borderColor: DIVIDER }}
                >
                  <span className="text-[17px] leading-tight text-white">{day.dayTitle}</span>
                  <span className="text-[17px] leading-tight text-white">{day.headerLabel ?? 'Принцип:'}</span>
                </div>
                <div className="mt-4 flex justify-between gap-6">
                  <ul className="min-w-0 flex-1 list-none space-y-3">
                    {day.exercises.map((ex, i) => {
                      const parts = ex.split(/\s*\/\s*/).map((p) => p.trim()).filter(Boolean);
                      return (
                        <li key={i} className="text-[17px] leading-tight text-white">
                          {parts.map((part, j) => {
                            const link = getExerciseLink(part);
                            const href = link
                              ? `/module/${link.moduleId}/exercise/${slugify(link.name)}`
                              : null;
                            return (
                              <span key={j}>
                                {j > 0 && ' / '}
                                {href ? (
                                  <Link
                                    to={href}
                                    state={{ name: link!.name }}
                                    className="text-white transition-opacity hover:opacity-80"
                                  >
                                    {part}
                                  </Link>
                                ) : (
                                  part
                                )}
                              </span>
                            );
                          })}
                        </li>
                      );
                    })}
                  </ul>
                  <div className="w-[170px] shrink-0 space-y-2 text-right">
                    {day.principleLines.map((line, i) => (
                      <p key={i} className="text-[17px] leading-tight text-white">
                        {line}
                      </p>
                    ))}
                  </div>
                </div>
                {day.note && (
                  <p className="mt-5 text-[17px] leading-tight text-white">{day.note}</p>
                )}
              </div>
            ))}
          </div>
        ) : (
          <p className="text-[19px] leading-tight text-white opacity-90">
            График для этой программы будет добавлен позже.
          </p>
        )}

        <div className="mt-[60px] flex w-[280px] max-w-full items-center justify-center gap-6">
          <Link
            to={backUrl}
            className="flex h-10 w-10 items-center justify-center rounded-full border transition-opacity hover:opacity-90"
            style={{
              borderColor: 'rgb(235,235,240)',
              backgroundColor: NAV_BG,
              color: NAV_TEXT,
            }}
            aria-label="Назад к программе"
          >
            <ChevronLeftIcon />
          </Link>
          <Link
            to="/"
            className="flex h-10 items-center justify-center rounded-[8px] border px-4 transition-opacity hover:opacity-90"
            style={{
              backgroundColor: NAV_BG,
              borderColor: NAV_BORDER,
              color: NAV_TEXT,
            }}
          >
            <span className="text-[16px]">Главная</span>
          </Link>
        </div>
      </div>
    </div>
  );
}
