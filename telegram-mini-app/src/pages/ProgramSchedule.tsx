/** Поиск по ключу без учёта регистра (для кириллицы) */
function getExerciseLink(displayName: string): { moduleId: string; name: string } | null {
  const trimmed = displayName.trim();
  const keyLower = trimmed.toLowerCase();
  return (
    FHT_EXERCISE[trimmed] ??
    FHT_EXERCISE[keyLower] ??
    Object.entries(FHT_EXERCISE).find(([k]) => k.toLowerCase() === keyLower)?.[1] ??
    null
  );
}

