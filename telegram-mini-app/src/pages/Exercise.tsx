export default function Exercise() {
  const { moduleId, levelId, exerciseSlug } = useParams<{
    moduleId: string;
    levelId?: string;
    exerciseSlug: string;
  }>();
  const location = useLocation();
  const moduleContent =
    exerciseSlug && moduleId === '3' ? MODULE3_EXERCISE_CONTENT[exerciseSlug]
    : exerciseSlug && moduleId === '4' ? MODULE4_EXERCISE_CONTENT[exerciseSlug]
    : undefined;
  const title =
    moduleContent?.title ??
    (location.state as { name?: string } | null)?.name ??
    exerciseSlug ??
    'Упражнение';

  const backUrl = moduleId && levelId
    ? `/module/${moduleId}/level/${levelId}/schedule`
    : moduleId
      ? `/module/${moduleId}`
      : '/';

  const exerciseNames = moduleId ? (MODULES[moduleId]?.exercises ?? []) : [];
  const slugs = exerciseNames.map((name) => slugify(name));
  const currentIndex = exerciseSlug ? slugs.indexOf(exerciseSlug) : -1;
  const hasNext = currentIndex >= 0 && currentIndex < slugs.length - 1;
  const nextSlug = hasNext ? slugs[currentIndex + 1] : null;
  const nextName = hasNext ? exerciseNames[currentIndex + 1] : null;
  const nextUrl =
    moduleId && nextSlug
      ? levelId
        ? `/module/${moduleId}/level/${levelId}/exercise/${nextSlug}`
        : `/module/${moduleId}/exercise/${nextSlug}`
      : null;

  const exerciseHref =
    moduleId && exerciseSlug
      ? levelId
        ? `/module/${moduleId}/level/${levelId}/exercise/${exerciseSlug}`
        : `/module/${moduleId}/exercise/${exerciseSlug}`
      : '';
  const favoriteItem =
    moduleContent && exerciseHref
      ? { id: `exercise-${moduleId}-${exerciseSlug}`, title, href: exerciseHref }
      : null;

  const [mediaHeight, setMediaHeight] = useState(180);
  useEffect(() => {
    setMediaHeight(180);
  }, [exerciseSlug, moduleId]);

  const handleMediaLoaded = (width: number, height: number) => {
    if (height > width) setMediaHeight(340);
  };

  return (
    <div
      className="min-h-screen safe-area-padding pb-12"
      style={{ backgroundColor: BG_DARK, color: 'rgb(255,255,255)' }}
    >
      <div className="mx-auto max-w-[440px] px-[25px] pt-[20px]">
        <div className="flex items-center justify-between gap-2">
          <BackButton />
          {favoriteItem ? (
            <FavoriteButton item={favoriteItem} className="h-10 w-10 shrink-0 rounded-full" />
          ) : null}
        </div>
        <h1
          className="mt-8 mb-6 text-[19px] leading-tight"
          style={{
            color: GREEN,
            fontFamily: '"SF Pro", -apple-system, BlinkMacSystemFont, sans-serif',
          }}
        >
          {title}
        </h1>
        {moduleContent && (
          <div
            className="mb-6 w-full max-w-[345px] overflow-hidden rounded-lg"
            style={{
              height: mediaHeight,
              backgroundColor: (moduleContent as { imageUrl?: string }).imageUrl ? 'transparent' : EXERCISE_PLACEHOLDER_BG,
            }}
            aria-hidden
          >
            {(moduleContent as { imageUrl?: string }).imageUrl ? (
              /\.(mp4|mov)$/i.test((moduleContent as { imageUrl: string }).imageUrl) ? (
                <video
                  src={getMediaUrl((moduleContent as { imageUrl: string }).imageUrl, { basePrefix: getMediaBaseUrl(location.pathname) })}
                  className="h-full w-full object-contain"
                  autoPlay
                  loop
                  muted
                  playsInline
                  aria-hidden
                  onLoadedMetadata={(e) => {
                    const v = e.currentTarget;
                    if (v.videoWidth && v.videoHeight) handleMediaLoaded(v.videoWidth, v.videoHeight);
                  }}
                  onError={(e) => {
                    const path = (moduleContent as { imageUrl: string }).imageUrl;
                    const fallback = getMediaFallbackUrl(path, { basePrefix: getMediaBaseUrl(location.pathname) });
                    e.currentTarget.src = fallback;
                  }}
                />
              ) : (
                <img
                  src={getMediaUrl((moduleContent as { imageUrl: string }).imageUrl, { basePrefix: getMediaBaseUrl(location.pathname) })}
                  alt=""
                  className="h-full w-full object-contain"
                  onLoad={(e) => {
                    const img = e.currentTarget;
                    if (img.naturalWidth && img.naturalHeight) handleMediaLoaded(img.naturalWidth, img.naturalHeight);
                  }}
                  onError={(e) => {
                    const path = (moduleContent as { imageUrl: string }).imageUrl;
                    const fallback = getMediaFallbackUrl(path, { basePrefix: getMediaBaseUrl(location.pathname) });
                    e.currentTarget.src = fallback;
                  }}
                />
              )
            ) : null}
          </div>
        )}
        {moduleContent?.techniqueText ? (
          <p
            className="whitespace-pre-line text-[19px] leading-tight text-white opacity-90"
          >
            {moduleContent.techniqueText}
          </p>
        ) : (
          <p className="text-[19px] leading-tight text-white opacity-90">
            Страница упражнения. Контент будет добавлен позже.
          </p>
        )}
        <div className="mt-10 flex flex-wrap items-center justify-center gap-4 text-center">
          <Link
            to={backUrl}
            className="flex h-10 w-10 shrink-0 items-center justify-center rounded-full border transition-opacity hover:opacity-90"
            style={{
              borderColor: 'rgb(235,235,240)',
              backgroundColor: NAV_BG,
              color: 'rgb(30,30,30)',
            }}
            aria-label="Назад"
          >
            <ChevronLeftIcon />
          </Link>
          <Link
            to="/"
            className="flex h-10 items-center justify-center rounded-[8px] border px-4 transition-opacity hover:opacity-90"
            style={{
              backgroundColor: NAV_BG,
              borderColor: NAV_BORDER,
              color: 'rgb(30,30,30)',
            }}
          >
            <span className="text-[16px]">Главная</span>
          </Link>
          {nextUrl && nextName && (
            <Link
              to={nextUrl}
              state={{ name: nextName }}
              className="flex h-10 w-10 shrink-0 items-center justify-center rounded-full border text-left transition-opacity hover:opacity-90"
              style={{
                backgroundColor: NAV_BG,
                borderColor: NAV_BORDER,
                color: 'rgb(30,30,30)',
              }}
              aria-label="Следующее упражнение"
            >
              <ChevronRightIcon />
            </Link>
          )}
        </div>
      </div>
    </div>
  );
}
